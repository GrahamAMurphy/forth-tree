/* registers within MFP device */
#define gpip	0x00		/* data I/O port */
#define aer	0x02		/* active edge register */
#define ddr	0x04		/* data direction register */
#define iera	0x06		/* interrupt enable register a */
#define ierb	0x08		/* interrupt enable register b */
#define ipra	0x0a		/* interrupt pending register a */
#define iprb	0x0c		/* interrupt pending register b */
#define isra	0x0e		/* interrupt in-service register a */
#define isrb	0x10		/* interrupt in-service register b */
#define imra	0x12		/* interrupt mask register a */
#define imrb	0x14		/* interrupt mask register b */
#define vr	0x16		/* vector register */
#define tacr	0x18		/* timer a control register */
#define tbcr	0x1a		/* timer b control register */
#define tcdcr	0x1c		/* timers c and d control register */
#define tadr	0x1e		/* timer a data register */
#define tbdr	0x20		/* timer b data register */
#define tcdr	0x22		/* timer c data register */
#define tddr	0x24		/* timer d data register */
#define scr	0x26		/* sync character register */
#define ucr	0x28		/* usart control register */
#define rsr	0x2a		/* receiver status register */
#define tsr	0x2c		/* transmitter status register */
#define udr	0x2e		/* usart data register */

